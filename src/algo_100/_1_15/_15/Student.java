package algo_100._1_15._15;

public class Student {
    private String name;
    private String no;

    public String getName(){
        return name;
    }

    public void setName(){
        this.name = name;
    }

    public String getNo(){
        return no;
    }

    public void setNo(){
        this.no = no;
    }

    public Student(String name, String no){
        this.name = name;
        this.no = no;
    }
}
